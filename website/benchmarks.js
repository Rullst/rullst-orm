import './style.css';
import Chart from 'chart.js/auto';

document.addEventListener('DOMContentLoaded', () => {
  const container = document.getElementById('bench-charts-container');

  // Dynamically load the data.js script from the live GitHub Pages site
  // The CI benchmark action automatically updates this file on every push to main
  const script = document.createElement('script');
  script.src = 'https://rullst.github.io/rullst-orm/bench/data.js';
  script.onload = () => {
    if (window.BENCHMARK_DATA) {
      renderCharts(window.BENCHMARK_DATA);
    } else {
      container.innerHTML = '<div class="error">No benchmark data found.</div>';
    }
  };
  script.onerror = () => {
    container.innerHTML = '<div class="error">Failed to load benchmark data. Make sure CI has run successfully.</div>';
  };
  document.head.appendChild(script);

  function renderCharts(data) {
    container.innerHTML = ''; // Clear loading
    
    // data is usually an object where keys are benchmark suite names
    // For github-action-benchmark, it's typically: data.entries { BenchmarkName: [ { commit, date, tool, benches: [ {name, value, unit} ] } ] }
    
    // We handle the structure generated by github-action-benchmark
    const entries = data.entries || data;
    
    for (const [suiteName, history] of Object.entries(entries)) {
      if (!history || history.length === 0) continue;
      
      // Get all unique benchmark test names from the latest run
      const latestRun = history[history.length - 1];
      const testNames = latestRun.benches.map(b => b.name);
      
      testNames.forEach(testName => {
        // Create canvas container
        const card = document.createElement('div');
        card.className = 'feature-card glassmorphism bench-card';
        
        const title = document.createElement('h3');
        title.innerText = testName;
        card.appendChild(title);
        
        const canvas = document.createElement('canvas');
        card.appendChild(canvas);
        container.appendChild(card);
        
        // Prepare data for this specific test across history
        const labels = [];
        const dataPoints = [];
        
        history.forEach(run => {
          const bench = run.benches.find(b => b.name === testName);
          if (bench) {
            // Format date string beautifully
            const date = new Date(run.date);
            labels.push(`${date.getMonth()+1}/${date.getDate()} - ${run.commit.id.substring(0,6)}`);
            dataPoints.push(bench.value);
          }
        });
        
        const unit = latestRun.benches.find(b => b.name === testName)?.unit || 'ns/iter';

        // Draw Chart
        new Chart(canvas, {
          type: 'line',
          data: {
            labels: labels,
            datasets: [{
              label: `${testName} (${unit})`,
              data: dataPoints,
              borderColor: '#f9a826', // Neon orange
              backgroundColor: 'rgba(249, 168, 38, 0.2)',
              borderWidth: 2,
              pointBackgroundColor: '#00f0ff',
              pointBorderColor: '#00f0ff',
              pointRadius: 4,
              fill: true,
              tension: 0.3
            }]
          },
          options: {
            responsive: true,
            plugins: {
              legend: {
                display: false
              },
              tooltip: {
                backgroundColor: 'rgba(0, 0, 0, 0.8)',
                titleColor: '#00f0ff',
                bodyColor: '#fff',
                borderColor: 'rgba(255, 255, 255, 0.1)',
                borderWidth: 1
              }
            },
            scales: {
              y: {
                beginAtZero: true,
                grid: { color: 'rgba(255, 255, 255, 0.05)' },
                ticks: { color: '#a0aec0' }
              },
              x: {
                grid: { display: false },
                ticks: { color: '#a0aec0', maxRotation: 45, minRotation: 45 }
              }
            }
          }
        });
      });
    }
  }
});
